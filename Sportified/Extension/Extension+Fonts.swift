import SwiftUI

extension Font {
    
    static func mulishBlack(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-Black", size: size)!)
    }

    static func mulishBold(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-Bold", size: size)!)
    }
    
    static func mulishItalic(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-MediumItalic", size: size)!)
    }
    
    static func mulishExtraBold(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-ExtraBold", size: size)!)
    }
    
    static func mulishMedium(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-Medium", size: size)!)
    }
    
    static func mulishRegular(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-Regular", size: size)!)
    }
    static func mulishSemiBold(size: CGFloat) -> Font {
        return Font(UIFont(name: "Mulish-SemiBold", size: size)!)
    }
    
}
